CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_obtenerLibro`(IN vidlibro INT)
BEGIN
	SELECT l.idlibro,l.nombre,l.existencia,l.precio,l.descripcion,a.nombre as autor ,e.nombre as editorial ,g.nombre as genero FROM libro l 
    INNER JOIN autor a ON l.idautor=a.idautor 
